class calculator:
    def add(self,a,b):
        print("Add:",a+b)

    def sub(self,a,b):
        print("sub:",a-b)

    def mul(self,a,b):
        print("Mul:",a*b)

    def div(self,a,b):
        print("Div:",a/b)

add=calculator()
add.add(34,51)

sub=calculator()
sub.sub(29,13)

mul=calculator()
mul.mul(12,62)

div=calculator()
div.div(354,5)
        
